/*
 WHILE LOOP

1. Initialize the counter
2. while (condition) {execute; increment or decrement counter;}

 */

void main() {
  // WHILE Loop
  // Find the even numbers between 1 and 10

  var i = 1;
  while (i <= 10) {
    //code
    if (i % 2 == 0) {
      print(i);
    }
    //i = i +1;
    i++;
  }
}
